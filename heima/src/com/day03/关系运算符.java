package com.day03;

public class 关系运算符 {
    public static void main(String[] args) {
        /*==,!=,>,>=,<,<= 正确为true 错误false*/
        int a=10,b=12;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
    }
}
